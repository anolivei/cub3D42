void	render_map(t_all *all)
{
	int	i;
	int	j;
	int	tile_color;

	i = 0;
	while (i < MAP_NUM_ROWS)
	{
		j = 0;
		while (j < MAP_NUM_COLS)
		{
			all->map.tile_x = j * TILE_SIZE;
			all->map.tile_y = i * TILE_SIZE;
			tile_color = map[i][j] != 0 ? RED : WHITE;
			draw_square(all, tile_color);
			j++;
		}
		i++;
	}
}